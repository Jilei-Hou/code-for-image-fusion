function E=rc_up2(A)
%-------------------------------------------------------------------------%
%函数功能：对A进行2上采样（A是二值图像）
% 参数说明：
% A：输入二值图像
% 输出：
% E：2上采样结果
%-------------------------------------------------------------------------%

[m,n]=size(A);
E=zeros(2*m,2*n);
for j=1:2*m
    for i=1:2*n
        E(j,i)=A(ceil(j/2),ceil(i/2));
    end
end
